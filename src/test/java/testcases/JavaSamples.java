package testcases;

public class JavaSamples {

	public static void main(String args[]) {
		int i = 9;

		System.out.println(i);   //9
		System.out.println(i--); //9
		System.out.println(i);   //8
		System.out.println(i);   //8

		System.out.println();
		
		
		
		i = 9;
		System.out.println(i);   //9
		System.out.println(--i); //8
		System.out.println(i);   //8
		System.out.println(i);   //8


		
		System.out.println(i);   //8
		System.out.println(i++); //8
		System.out.println(i);   //9
		System.out.println(i);   //9

		System.out.println();
		
		
		
		i = 9;
		System.out.println(i);   //9
		System.out.println(++i); //10
		System.out.println(i);   //10
		System.out.println(i);   //10

	}

}
